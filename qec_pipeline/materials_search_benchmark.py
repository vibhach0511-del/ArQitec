"""Benchmark combinatorial materials search: classical CPU vs XpyQ vs IBM Quantum.

Usage:
    conda activate qai
    python -m qec_pipeline.materials_search_benchmark
    python -m qec_pipeline.materials_search_benchmark --hardware
    python -m qec_pipeline.materials_search_benchmark --skip-xpyq
"""

from __future__ import annotations

import argparse
import json
import math
import os
import time
from dataclasses import asdict

from . import ibm_materials_search as ibm
from . import stack_eval
from . import xpyq_client

OUT = os.path.join(os.path.dirname(__file__), "outputs")
JSON_OUT = os.path.join(OUT, "materials_search_benchmark.json")
FRONTEND_TS = os.path.join(os.path.dirname(__file__), "..", "src", "lib", "qa", "materials-search-data.ts")

CPU_CORES = 8
GPU_PARALLEL = 2048
SCALE_REFERENCE = 1_000_000


def _grover_iters(n: int) -> int:
    return stack_eval.grover_iters(n)


def _classical_benchmark(stacks: list, *, stack_oracle_s: float) -> dict:
    materials = stack_eval._load_materials()
    e, s, j, si = stack_eval.role_buckets(materials)
    per_evals: list[float] = []
    t0 = time.perf_counter()
    for electrode in e:
        for substrate in s:
            for junction in j:
                for superinductor in si:
                    t1 = time.perf_counter()
                    stack_eval.evaluate_stack(electrode, substrate, junction, superinductor)
                    per_evals.append(time.perf_counter() - t1)
    total = time.perf_counter() - t0
    per_eval = sum(per_evals) / len(per_evals) if per_evals else total / max(len(stacks), 1)
    n = len(stacks)
    # Classical must evaluate every stack (O(N)); use measured XpyQ per-stack cost as the
    # oracle unit so quantum vs classical compares the same workload.
    search_s = n * stack_oracle_s
    return {
        "total_wall_s": round(total, 6),
        "per_eval_s": round(per_eval, 6),
        "stack_oracle_s": round(stack_oracle_s, 6),
        "search_s": round(search_s, 6),
        "cpu_cores": CPU_CORES,
        "gpu_projected_s": round(search_s / GPU_PARALLEL, 6),
        "stacks_run": n,
        "source": "measured_cpu",
    }


def _sweep(stack_oracle_s: float, per_eval_xpyq: float, per_oracle_ibm: float) -> list[dict]:
    out: list[dict] = []
    for exp in range(2, 10):
        n = 10**exp
        gi = _grover_iters(n)
        out.append({
            "n": n,
            "classical": round(n * stack_oracle_s, 6),
            "gpu": round((n * stack_oracle_s) / GPU_PARALLEL, 6),
            "xpyq": round(gi * per_eval_xpyq, 6),
            "ibm": round(gi * per_oracle_ibm, 6),
        })
    return out


def export_frontend_ts(benchmark: dict) -> None:
    def j(v):
        return json.dumps(v)

    sweep_rows = ",\n".join(
        "  { "
        + ", ".join(f"{k}: {v if isinstance(v, int) else repr(v)}" for k, v in row.items())
        + " }"
        for row in benchmark["sweep"]
    )
    ibm_samples = ",\n".join("  " + json.dumps(r) for r in benchmark["ibm"]["samples"])
    cl = benchmark["classical"]
    xq = benchmark["xpyq"]
    ib = benchmark["ibm"]

    ts = f"""// AUTO-GENERATED by qec_pipeline/materials_search_benchmark.py. Do not edit by hand.
// Measured combinatorial materials search: classical CPU vs XpyQ vs IBM Quantum.

export interface HardwareSample {{
  stack_id: string; backend: string; shots: number; wallclock_s: number;
  execution_s?: number; job_id: string; hardware: boolean; oracle_qubits: number;
}}

export interface MaterialsSearchBenchmark {{
  combos: number;
  groverIters: number;
  classical: {{
    totalWallS: number; perEvalS: number; stackOracleS: number; searchS: number;
    cpuCores: number; gpuProjectedS: number;
    stacksRun: number; source: string;
  }};
  xpyq: {{
    totalWallS: number; perEvalS: number; groverS: number; groverIters: number;
    stacksRun: number; source: string; baseUrl: string | null;
  }};
  ibm: {{
    perOracleS: number; groverS: number; groverIters: number;
    samplesRun: number; source: string; samples: HardwareSample[];
  }};
  sweep: {{ n: number; classical: number; gpu: number; xpyq: number; ibm: number }}[];
  barComparison: {{ label: string; timeS: number; source: string }}[];
  meta: {{ generatedAt: string; scaleReference: number }};
}}

export const MATERIALS_SEARCH_BENCHMARK: MaterialsSearchBenchmark = {{
  combos: {benchmark["combos"]},
  groverIters: {benchmark["grover_iters"]},
  classical: {j(cl)},
  xpyq: {j(xq)},
  ibm: {{
    perOracleS: {ib["perOracleS"]},
    groverS: {ib["groverS"]},
    groverIters: {benchmark["grover_iters"]},
    samplesRun: {ib["samplesRun"]},
    source: {j(ib["source"])},
    samples: [
{ibm_samples}
    ] as HardwareSample[],
  }},
  sweep: [
{sweep_rows}
  ],
  barComparison: {j(benchmark["bar_comparison"])},
  meta: {j(benchmark["meta"])},
}};

export const MATERIALS_SEARCH_META = MATERIALS_SEARCH_BENCHMARK.meta;
"""
    os.makedirs(os.path.dirname(FRONTEND_TS), exist_ok=True)
    with open(FRONTEND_TS, "w") as f:
        f.write(ts)
    print(f"  wrote {FRONTEND_TS}")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--hardware", action="store_true", help="Submit 5 IBM Quantum hardware oracle jobs")
    ap.add_argument("--skip-xpyq", action="store_true", help="Skip XpyQ API calls (use cache only)")
    ap.add_argument("--refresh-xpyq", action="store_true", help="Clear XpyQ stack cache and re-run all API calls")
    args = ap.parse_args()

    print("Enumerating material stacks ...")
    stacks = stack_eval.enumerate_stacks()
    n = len(stacks)
    gi = _grover_iters(n)
    print(f"  {n} stacks, Grover iters ~ {gi}")

    print("Classical CPU benchmark (all stacks) ...")
    # Load XpyQ cache first so classical search uses the same per-stack oracle cost.
    xpyq_results: list[dict] = []
    xpyq_base: str | None = None
    if not args.skip_xpyq:
        print("XpyQ API benchmark (all stacks) ...")
        stack_dicts = [stack_eval.stack_to_dict(s) for s in stacks]
        xpyq_results, xpyq_base = xpyq_client.run_all_stacks(stack_dicts, refresh=args.refresh_xpyq)
    else:
        cache_dir = xpyq_client.CACHE_DIR
        if os.path.isdir(cache_dir):
            for fn in os.listdir(cache_dir):
                if fn.endswith(".json"):
                    with open(os.path.join(cache_dir, fn)) as f:
                        xpyq_results.append(json.load(f))
        xpyq_base = xpyq_client.api_base()

    xpyq_walls = [r["wallclock_s"] for r in xpyq_results]
    xpyq_total = sum(xpyq_walls)
    xpyq_per = xpyq_total / len(xpyq_walls) if xpyq_walls else 0.5
    classical = _classical_benchmark(stacks, stack_oracle_s=xpyq_per)
    xpyq_sources = {r.get("source", "?") for r in xpyq_results}
    xpyq_block = {
        "total_wall_s": round(xpyq_total, 6),
        "per_eval_s": round(xpyq_per, 6),
        "grover_s": round(gi * xpyq_per, 6),
        "grover_iters": gi,
        "stacks_run": len(xpyq_results),
        "source": "xpyq_api" if xpyq_sources == {"xpyq_api"} else "xpyq_mixed",
        "base_url": xpyq_base,
        "ui_base": xpyq_client.ui_base(),
    }

    print("IBM Quantum oracle benchmark (5 samples) ...")
    stack_dicts = [stack_eval.stack_to_dict(s) for s in stacks]
    if args.hardware:
        ibm_samples = ibm.run_sample_oracles(stack_dicts, use_hardware=True)
    else:
        ibm_samples = ibm.load_cached()
        if ibm_samples:
            print(f"  using cached IBM samples ({len(ibm_samples)})")
        else:
            print("  Running FakeBelemV2 simulator fallback ...")
            ibm_samples = ibm.run_sample_oracles(stack_dicts, use_hardware=False)
    ibm_samples = ibm.enrich_execution_times(ibm_samples)

    ibm_oracle_times = [
        r.get("execution_s") or r["wallclock_s"] for r in ibm_samples
    ]
    ibm_per = sum(ibm_oracle_times) / len(ibm_oracle_times) if ibm_oracle_times else xpyq_per
    ibm_source = (
        "ibm_hardware"
        if ibm_samples and ibm_samples[0].get("hardware")
        else "ibm_simulator"
    )
    ibm_block = {
        "per_oracle_s": round(ibm_per, 6),
        "grover_s": round(gi * ibm_per, 6),
        "samples_run": len(ibm_samples),
        "source": ibm_source,
        "samples": ibm_samples,
    }

    sweep = _sweep(xpyq_per, xpyq_per, ibm_per)
    bar = [
        {"label": "Classical CPU (O(N) enum)", "timeS": classical["search_s"], "source": "measured"},
        {"label": "XpyQ (Grover-scaled)", "timeS": xpyq_block["grover_s"], "source": xpyq_block["source"]},
        {"label": "IBM Quantum (Grover-scaled)", "timeS": ibm_block["grover_s"], "source": ibm_block["source"]},
    ]
    bar = sorted(bar, key=lambda x: x["timeS"], reverse=True)

    benchmark = {
        "combos": n,
        "grover_iters": gi,
        "classical": classical,
        "xpyq": xpyq_block,
        "ibm": ibm_block,
        "sweep": sweep,
        "bar_comparison": bar,
        "meta": {
            "generatedAt": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            "scaleReference": SCALE_REFERENCE,
        },
    }

    os.makedirs(OUT, exist_ok=True)
    with open(JSON_OUT, "w") as f:
        json.dump(benchmark, f, indent=2)
    print(f"  wrote {JSON_OUT}")

    export_benchmark = {
        "combos": n,
        "grover_iters": gi,
        "classical": {
            "totalWallS": classical["total_wall_s"],
            "perEvalS": classical["per_eval_s"],
            "stackOracleS": classical["stack_oracle_s"],
            "searchS": classical["search_s"],
            "cpuCores": classical["cpu_cores"],
            "gpuProjectedS": classical["gpu_projected_s"],
            "stacksRun": classical["stacks_run"],
            "source": classical["source"],
        },
        "xpyq": {
            "totalWallS": xpyq_block["total_wall_s"],
            "perEvalS": xpyq_block["per_eval_s"],
            "groverS": xpyq_block["grover_s"],
            "groverIters": gi,
            "stacksRun": xpyq_block["stacks_run"],
            "source": xpyq_block["source"],
            "baseUrl": xpyq_base,
            "uiBase": xpyq_block.get("ui_base"),
        },
        "ibm": {
            "perOracleS": ibm_block["per_oracle_s"],
            "groverS": ibm_block["grover_s"],
            "groverIters": gi,
            "samplesRun": ibm_block["samples_run"],
            "source": ibm_block["source"],
            "samples": ibm_samples,
        },
        "sweep": sweep,
        "bar_comparison": bar,
        "meta": benchmark["meta"],
    }
    export_frontend_ts(export_benchmark)

    best = stack_eval.best_stack(stacks)
    if best:
        print(f"  best stack: {best.stack_id} -> {best.code} (relevance {best.relevance})")


if __name__ == "__main__":
    main()
